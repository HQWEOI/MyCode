public class Add {
	public int Add(int num1, int num2) {
		return num1 + num2;
	}
	public long Add(long num1, long num2) {
		return num1 + num2;
	}
	public double Add(double num1, double num2) {
		return num1 + num2;
	}
	public float Add(float num1, float num2) {
		return num1 + num2;
	}
}
